<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use App\Models\Product;
use App\Models\Product_details; // ƒê·ªïi t√™n model cho chu·∫©n (kh√¥ng _)

class CartController extends Controller
{
    // üëâ Hi·ªÉn th·ªã gi·ªè h√†ng
    public function cart()
    {
        $cart = session()->get('cart', []);
        return view('user.cart', compact('cart'));
    }

    // üëâ Th√™m s·∫£n ph·∫©m chi ti·∫øt v√†o gi·ªè h√†ng
    public function addDetail(Request $request)
    {
        $request->validate([
            'product_detail_id' => 'required|exists:product_details,id',
            'quantity'          => 'required|integer|min:1',
            'size'              => 'required|string|max:50',
        ]);

        $detail = Product_details::findOrFail($request->product_detail_id);
        $cart   = session()->get('cart', []);

        $key = "{$detail->id}-{$detail->size}-{$detail->color}";

        if (isset($cart[$key])) {
            $cart[$key]['quantity'] += $request->quantity;
        } else {
            $cart[$key] = [
                'product_detail_id' => $detail->id,
                'product_name'      => $detail->product->name,
                'size'              => $detail->size,
                'color'             => $detail->color,
                'price'             => $detail->price,
                'quantity'          => $request->quantity,
                'image'             => $detail->image,
            ];
        }

        session()->put('cart', $cart);

        return back()->with('success', 'ƒê√£ th√™m v√†o gi·ªè h√†ng!');
    }


    // üëâ X√≥a s·∫£n ph·∫©m kh·ªèi gi·ªè h√†ng
    public function remove($key)
    {
        $cart = session()->get('cart', []);
        if (isset($cart[$key])) {
            unset($cart[$key]);
            session()->put('cart', $cart);
        }
        return back()->with('success', 'ƒê√£ x√≥a s·∫£n ph·∫©m kh·ªèi gi·ªè h√†ng.');
    }

    // üëâ C·∫≠p nh·∫≠t s·ªë l∆∞·ª£ng
    public function update(Request $request)
    {
        $cart = session()->get('cart', []);
        foreach ($request->quantities as $key => $qty) {
            $qty = (int) $qty;
            if ($qty <= 0) {
                unset($cart[$key]);
            } elseif (isset($cart[$key])) {
                $cart[$key]['quantity'] = $qty;
            }
        }
        session()->put('cart', $cart);
        return redirect()->route('cart')->with('success', 'Gi·ªè h√†ng ƒë√£ ƒë∆∞·ª£c c·∫≠p nh·∫≠t!');
    }

    // üëâ X√≥a s·∫°ch gi·ªè h√†ng
    public function clear()
    {
        session()->forget('cart');
        return back()->with('success', 'ƒê√£ l√†m s·∫°ch gi·ªè h√†ng.');
    }
}
